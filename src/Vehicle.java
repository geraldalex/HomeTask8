public abstract class Vehicle {
    int coordinateX;
    int coordinateY;
    int coast;
    int speed;
    int yearOfMonufacture;

    public Vehicle(int coordinateX, int coordinateY, int coast, int speed, int yearOfMonufacture) {
        this.coordinateX = coordinateX;
        this.coordinateY = coordinateY;
        this.coast = coast;
        this.speed = speed;
        this.yearOfMonufacture = yearOfMonufacture;
    }




}